from typing import Dict, List, Tuple

from common.data_classes.evaluation import CorrectnessType
from common.data_classes.qa import QuestionAnswerPair, Proof
from common.data_classes.rag_system import Chunk
from rouge_score import rouge_scorer



def get_rouge_l_score(generated_answer: str, reference_answer: str) -> float:
    """
    Calculate ROUGE-L F1 score between generated and reference answers.
    
    Args:
        generated_answer: The answer generated by the model
        reference_answer: The reference/correct answer
        
    Returns:
        ROUGE-L F1 score (0.0 to 1.0), or 0.0 if rouge-score is not available
    """
    
    try:
        scorer = rouge_scorer.RougeScorer(['rougeL'], use_stemmer=True)
        scores = scorer.score(reference_answer, generated_answer)
        return scores['rougeL'].fmeasure
    except Exception as e:
        print(f"Warning: Error calculating ROUGE-L score: {e}")
        return 0.0


def get_correctness(qa_pair: QuestionAnswerPair, generated_answer: str) -> CorrectnessType:
    # Normalize case for comparison
    normalized_generated = generated_answer.strip().lower()
    normalized_correct = qa_pair.correct_answer.strip().lower()
    
    # First check if it matches the correct answer directly
    if normalized_correct == normalized_generated:
        return CorrectnessType.CORRECT
    
    # For multiple choice questions, check if the generated answer matches the correct choice
    if qa_pair.choices:
        # Find the correct choice
        correct_choice = None
        for choice in qa_pair.choices:
            if choice.label.strip().lower() == normalized_correct:
                correct_choice = choice
                break
        
        # If we found the correct choice, check if generated answer matches its text
        if correct_choice and correct_choice.text.strip().lower() == normalized_generated:
            return CorrectnessType.CORRECT
        
        # Check if it matches any other choice (indicating wrong answer)
        for choice in qa_pair.choices:
            if choice.label.strip().lower() == normalized_generated:
                return CorrectnessType.WRONG
            if choice.text.strip().lower() == normalized_generated:
                return CorrectnessType.WRONG
    
    # For open-ended questions or if no choice matches, it's a formatting error
    return CorrectnessType.FORMATTING_ERROR


def get_correctness_and_rouge_l(qa_pair: QuestionAnswerPair, generated_answer: str) -> Tuple[CorrectnessType, float]:
    """
    Get both binary correctness and ROUGE-L score for a generated answer.
    
    Args:
        qa_pair: The question-answer pair containing the reference answer
        generated_answer: The answer generated by the model
        
    Returns:
        Tuple of (correctness_type, rouge_l_score)
    """
    # Get binary correctness
    correctness = get_correctness(qa_pair, generated_answer)
    
    # Get reference answer for ROUGE-L calculation
    reference_answer = qa_pair.correct_answer
    
    # For multiple choice questions, if correct_answer is a label, get the corresponding text
    if qa_pair.choices:
        # Check if correct_answer is a label (A, B, C, etc.)
        for choice in qa_pair.choices:
            if choice.label.strip().lower() == qa_pair.correct_answer.strip().lower():
                reference_answer = choice.text
                break
    
    # Calculate ROUGE-L score
    rouge_l_score = get_rouge_l_score(generated_answer, reference_answer)
    
    return correctness, rouge_l_score

def _proof_is_grounded_in_chunk(proof: str, chunk: str) -> bool:
    proof_words = proof.strip().split()
    chunk_words = chunk.strip().split()

    proof_len = len(proof_words)

    # If proof has fewer than 8 words, check if the entire sequence exists in chunk
    if proof_len < 8:
        for i in range(len(chunk_words) - proof_len + 1):
            if chunk_words[i:i + proof_len] == proof_words:
                return True
        return False

    # Otherwise, check for any 8-word matching sequence
    for i in range(proof_len - 7):
        sub_seq = proof_words[i:i + 8]
        for j in range(len(chunk_words) - 7):
            if chunk_words[j:j + 8] == sub_seq:
                return True

    return False


def get_proof_map(
        qa_pair: QuestionAnswerPair,
        chunks: List[Chunk]
) -> Dict[Proof, int]:
    proofs = qa_pair.proofs
    proof_map: Dict[Proof, int] = {p: -1 for p in proofs}

    for chunk_rank, chunk in enumerate(chunks):
        c_txt = chunk.text
        for proof in proofs:
            if proof_map[proof] != -1:  # already grounded
                continue
            if _proof_is_grounded_in_chunk(proof.context, c_txt):
                proof_map[proof] = chunk_rank

        if all(v != -1 for v in proof_map.values()):
            break

    return proof_map